    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
        String DelID = TfDelPro.getText().trim();
        if(DelID.isBlank()){
            JOptionPane.showMessageDialog(this, "You didn't choose your product which you wanted to delete !!");
            return;
        }
        if(!isNum(DelID)){
            JOptionPane.showMessageDialog(this, "ID must be number");
            return;
        }
        
        int id = Integer.parseInt(DelID);
        boolean have = false;
        for(int i = 0; i < cart.size(); i++){
            if(cart.get(i).getVegetableID() == id){
                int result = JOptionPane.showConfirmDialog(this, "Sure to delete " + cart.get(i).getVegetableName());
                if(result == JOptionPane.YES_OPTION){
                    int amount = cart.get(i).getAmount();
                    for (Vegetable vegetable : BillView.prod.Product) {
                        if(vegetable.getVegetableID() == cart.get(i).getVegetableID()){
                            int ProductAmount = vegetable.getAmount();
                            vegetable.setAmount(amount+ProductAmount);
                            break;
                        }
                    }
                    cart.remove(i);
                    
                    JOptionPane.showMessageDialog(this, "Delete Successfully!!");
                    have = false;
                    break;
                }
            }
            else 
                have = true;
        }
        if(have){
            JOptionPane.showMessageDialog(this, "Delete Fail!!");
        }
        BillView.prod.reloadTable();
        loadTable();
    }//GEN-LAST:event_btnDelActionPerformed

