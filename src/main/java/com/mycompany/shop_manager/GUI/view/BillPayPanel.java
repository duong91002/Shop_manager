    private void btnPayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPayActionPerformed
        // TODO add your handling code here:
        try{
            String tcus = cbCustomer.getSelectedItem().toString();
            String[] cus = tcus.split("\\.");

            float total = Float.parseFloat(TfTotal.getText());

            String note = TaNote.getText();

            long millis = System.currentTimeMillis();
            Date date = new Date(millis);
            
            if(cart.isEmpty()){
                JOptionPane.showMessageDialog(this, "You didn't get anything!!");
                return;
            }

            OrderBLL or = new OrderBLL();
            List<Customers> listcus = or.loadcustomer();
            Customers c = new Customers();
            for (Customers customers : listcus) {
                if(customers.getCustomerID() == Integer.parseInt(cus[0]) && customers.getFullname().equalsIgnoreCase(cus[1])){
                    c = customers;
                    break;
                }
            }

            List<Order> listorder = or.loadOrders();
            Order order = new Order();
            order.setDate(date);
            order.setNote(note);
            order.setTotal(total);
            order.setCustomer(c);
            order.setOrderID(listorder.size());
            
            ArrayList<Orderdetail> listorderdetail = new ArrayList<Orderdetail>();
            for (Vegetable ve : cart) {
                Orderdetail orderdetail = new Orderdetail();
                orderdetail.setOrder(order);
                orderdetail.setPrice(ve.getPrice());
                orderdetail.setQuantity(ve.getAmount());
                orderdetail.setVegetable(ve);
                orderdetail.setOrderID(order.getOrderID());
                orderdetail.setVegetableID(ve.getVegetableID());
                listorderdetail.add(orderdetail);
            }
            order.setListOrderDetail(listorderdetail);
            String resultadd = or.addOrder(order,order.getOrderID());
            if(resultadd.equals("Success")){
                for (Orderdetail orderdetail : listorderdetail) {
                    or.addOrderDetail(orderdetail);
                }
            }
            else{
                JOptionPane.showMessageDialog(this, "Pay Fail!!");
                return;
            }
            List<Vegetable> vegetable = or.loadProducts();
            for (Vegetable vegeCart : cart) {
                for (Vegetable vegeHave : vegetable) {
                    if(vegeCart.getVegetableID() == vegeHave.getVegetableID()){
                        int oldquantity = vegeHave.getAmount();
                        int buyquantity = vegeCart.getAmount();
                        vegeHave.setAmount(oldquantity-buyquantity);
                        or.updateVegetable(vegeHave);
                        break;
                    }
                }
            }
            
            BillProductPanel.Cart.clear();
            ChangeCart();
            BillView.prod.changeProducts();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Pay Fail!!");
            return;
        }
        JOptionPane.showMessageDialog(this, "SuccessFully Pay!!");
    }//GEN-LAST:event_btnPayActionPerformed

