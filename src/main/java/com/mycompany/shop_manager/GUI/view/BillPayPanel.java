    private void loadTable(){
        OrderBLL Or = new OrderBLL();
        DefaultTableModel model = Or.convertVegetable(cart);
        jTable1.setModel(model);
        
        int total = 0;
        if(!cart.isEmpty()){
            for(int i = 0; i < cart.size(); i++){
                total += cart.get(i).getPrice();
            }
        }
        String t = String.valueOf(total);
        TfTotal.setText(t);
    }
    
    private void Search(){
        String timkiem = TfSearch.getText().trim();
        if(timkiem.isBlank()){
            loadtablewithoutchangeTotal(cart);
            return;
        }
        if(!cart.isEmpty()){
            List<Vegetable> result = new ArrayList<Vegetable>();
            for (Vegetable vegetable : cart) {
                if(vegetable.getVegetableName().toLowerCase().trim().contains(timkiem.toLowerCase())){
                    result.add(vegetable);
                }
            }
            loadtablewithoutchangeTotal(result);
        }
    }
    private void loadtablewithoutchangeTotal(List<Vegetable> list){
        OrderBLL or = new OrderBLL();
        DefaultTableModel model = or.convertVegetable(list);
        jTable1.setModel(model);
    }
    
